export const SOUND_PACK_OPTIONS = PACKS.map(({ id, label }) => ({ id, label }));

function audioContextClass() {
  if (typeof window === "undefined") {
    return null;
  }

  return window.AudioContext || window.webkitAudioContext || null;
}

function readStoredEnabled() {
  try {
    const stored = window.localStorage?.getItem(ENABLED_STORAGE_KEY);
    return stored == null ? true : stored !== "false";
  } catch (_error) {
    return true;
  }
}

function writeStoredEnabled(enabled) {
  try {
    window.localStorage?.setItem(ENABLED_STORAGE_KEY, enabled ? "true" : "false");
  } catch (_error) {
    // Storage can be unavailable in private contexts; sound still works in memory.
  }
}

function packById(packId) {
  return (
    PACKS.find((candidate) => candidate.id === packId) ||
    PACKS.find((candidate) => candidate.id === DEFAULT_PACK_ID) ||
    PACKS[0]
  );
}

function readStoredPackId() {
  try {
    return packById(window.localStorage?.getItem(PACK_STORAGE_KEY)).id;
  } catch (_error) {
    return DEFAULT_PACK_ID;
  }
}

function writeStoredPackId(packId) {
  try {
    window.localStorage?.setItem(PACK_STORAGE_KEY, packId);
  } catch (_error) {
    // Non-persistent selection is fine when localStorage is blocked.
  }
}

function uniqueSources(soundPack) {
  const sources = new Set();

  Object.values(soundPack.cues).forEach((clips) => {
    clips.forEach((entry) => sources.add(entry.src));
  });

  return Array.from(sources);
}

async function decodeSource(context, src) {
  const response = await fetch(src);

  if (!response.ok) {
    throw new Error(`Unable to load sound asset: ${src}`);
  }

  return context.decodeAudioData(await response.arrayBuffer());
}

export function createSoundController() {
  const AudioContextClass = audioContextClass();
  let context = null;
  let contextStateListener = null;
  let master = null;
  let enabled = readStoredEnabled();
  let packId = readStoredPackId();
  let unlocked = false;
  let loading = false;
  let ready = false;
  let buffers = new Map();
  let loadToken = 0;
  let lastRecoveryAt = 0;
  const rotations = new Map();
  const supported = !!AudioContextClass && typeof fetch === "function";
  const listeners = new Set();

  const snapshot = () => ({ enabled, supported, unlocked, loading, ready, packId });

  const notify = () => {
    const state = snapshot();
    listeners.forEach((listener) => listener(state));
  };

  const clearBuffers = () => {
    loading = false;
    ready = false;
    buffers = new Map();
    rotations.clear();
  };

  const detachContextListener = () => {
    if (context && contextStateListener) {
      context.removeEventListener("statechange", contextStateListener);
    }

    contextStateListener = null;
  };

  const syncContextState = () => {
    if (!context) {
      unlocked = false;
      return;
    }

    if (context.state === "closed") {
      detachContextListener();
      context = null;
      master = null;
      clearBuffers();
      unlocked = false;
      return;
    }

    unlocked = context.state === "running";
  };

  const createContext = () => {
    context = new AudioContextClass();
    contextStateListener = () => {
      syncContextState();
      notify();
    };
    context.addEventListener("statechange", contextStateListener);

    master = context.createGain();
    master.gain.value = MASTER_VOLUME;
    master.connect(context.destination);

    syncContextState();
    return context;
  };

  const ensureContext = (forceFresh = false) => {
    if (!supported) {
      return null;
    }

    if (forceFresh) {
      detachContextListener();
      context = null;
      master = null;
      clearBuffers();
    } else {
      syncContextState();
    }

    if (!context) {
      return createContext();
    }

    if (!master) {
      master = context.createGain();
      master.gain.value = MASTER_VOLUME;
      master.connect(context.destination);
    }

    return context;
  };

  const loadSelectedPack = async (forceFresh = false) => {
    if (!enabled || !supported) {
      return false;
    }

    const ctx = ensureContext(forceFresh);

    if (!ctx) {
      return false;
    }

    syncContextState();

    if (!unlocked) {
      notify();
      return false;
    }

    const soundPack = packById(packId);
    const currentToken = loadToken + 1;
    loadToken = currentToken;
    clearBuffers();
    loading = true;
    notify();

    const decoded = await Promise.all(
      uniqueSources(soundPack).map(async (src) => {
        try {
          return [src, await decodeSource(ctx, src)];
        } catch (_error) {
          return [src, null];
        }
      })
    );

    if (loadToken !== currentToken || context !== ctx) {
      return false;
    }

    buffers = new Map(decoded.filter(([_src, buffer]) => !!buffer));
    loading = false;
    ready = buffers.size > 0;
    notify();
    return ready;
  };

  const recover = async (forceFresh = false) => {
    if (!enabled || !supported) {
      return false;
    }

    const now = Date.now();

    if (!forceFresh && now - lastRecoveryAt < RECOVERY_COOLDOWN_MS) {
      return ready && unlocked;
    }

    lastRecoveryAt = now;

    let ctx = ensureContext(forceFresh);

    if (!ctx) {
      return false;
    }

    try {
      if (ctx.state !== "running") {
        await ctx.resume();
      }
    } catch (_error) {
      ctx = ensureContext(true);

      if (!ctx) {
        return false;
      }

      try {
        if (ctx.state !== "running") {
          await ctx.resume();
        }
      } catch (_retryError) {
        syncContextState();
        notify();
        return false;
      }
    }

    syncContextState();
    notify();

    if (!unlocked) {
      return false;
    }

    if (ready && buffers.size > 0) {
      return true;
    }

    return loadSelectedPack();
  };

  const setEnabled = (nextEnabled) => {
    enabled = !!nextEnabled;
    writeStoredEnabled(enabled);

    if (!enabled) {
      notify();
      return Promise.resolve(false);
    }

    notify();
    return recover(true);
  };

  const setPack = (nextPackId) => {
    const nextPack = packById(nextPackId);
    packId = nextPack.id;
    writeStoredPackId(packId);
    loadToken += 1;
    clearBuffers();
    notify();

    if (enabled && unlocked) {
      return loadSelectedPack();
    }

    return Promise.resolve(false);
  };

  const chooseClip = (cueName) => {
    const choices = packById(packId).cues[cueName] || [];
    const available = choices.filter((entry) => buffers.has(entry.src));

    if (available.length === 0) {
      return null;
    }

    const index = rotations.get(cueName) || 0;
    rotations.set(cueName, index + 1);
    return available[index % available.length];
  };

  const play = (cueName, offset = 0) => {
    if (!enabled || !supported) {
      return false;
    }

    if (!unlocked || !ready || !master || !context || context.state !== "running") {
      void recover(false);
      return false;
    }

    const selectedClip = chooseClip(cueName);

    if (!selectedClip) {
      void recover(false);
      return false;
    }

    const buffer = buffers.get(selectedClip.src);

    if (!buffer) {
      void recover(false);
      return false;
    }

    try {
      const source = context.createBufferSource();
      const gain = context.createGain();

      source.buffer = buffer;
      source.playbackRate.value = selectedClip.playbackRate || 1;
      gain.gain.value = selectedClip.gain ?? 1;
      source.connect(gain);
      gain.connect(master);
      source.start(context.currentTime + offset);
      return true;
    } catch (_error) {
      clearBuffers();
      notify();
      void recover(false);
      return false;
    }
  };

  return {
    getSnapshot: snapshot,
    setEnabled,
    setPack,
    subscribe(listener) {
      listeners.add(listener);
      listener(snapshot());
      return () => listeners.delete(listener);
    },
    unlock() {
      return recover(true);
    },
    play,
    playMany(cues) {
      const uniqueCues = Array.from(new Set(cues));

      if (uniqueCues.length === 0) {
        return false;
      }

      if (!enabled || !supported) {
        return false;
      }

      if (!unlocked || !ready || !master || !context || context.state !== "running") {
        void recover(false);
        return false;
      }

      let played = false;

      uniqueCues.forEach((cueName, index) => {
        played = play(cueName, index * 0.13) || played;
      });

      return played;
    }
  };
}
